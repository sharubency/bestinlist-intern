# addition
def add(a,b):
    ans = a+b
    return ans

addition = add(5,6)
print("Addition of 5 and 6 is {0}".format(addition))

# maximum number

def maximum(a,b,c,d):
    result = max(a,b,c,d)
    return result
max_num = maximum(2,4,6,8)
print("maximum number of 2 4 6 8 is {0}".format(max_num))